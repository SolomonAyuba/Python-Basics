import math

# Basic Arithmetic

a = 10

b = 20

print("Addition:", a + b)

print("Subtraction:", a - b)

print("Multiplication:", a * b)

print("Division:", a / b)



# Power and Modulus

print("Power (a^2):", a ** 2)

print("Modulus (b % a):", b % a)



# Square Root, Factorial, and Logarithm

print("Square Root of a:", math.sqrt(a))

print("Factorial of 5:", math.factorial(5))

print("Natural Logarithm of a:", math.log(a))

print("Base 10 Logarithm of a:", math.log10(a))



# Trigonometry

angle = math.radians(45)  # converting 45 degrees to radians

print("Sine of 45 degrees:", math.sin(angle))

print("Cosine of 45 degrees:", math.cos(angle))

print("Tangent of 45 degrees:", math.tan(angle))


